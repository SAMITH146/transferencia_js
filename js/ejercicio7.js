// Ejercicio 7: Calcular el precio final de un producto con descuento

// Solicitar al usuario el precio del producto
let precioProd = prompt("Ingrese precio del producto:");
// Calcular el precio con descuento del 10%
let final = precioProd * 0.9;
// Mostrar el resultado
console.log("Precio con 10% de descuento: " + final);
alert("Precio con 10% de descuento: " + final);
