// Ejercicio 4: Calcular el consumo de combustible de un vehículo

// Solicitar al usuario los kilómetros recorridos y los litros consumidos
let KmRecorridos = prompt("Ingrese los kilómetros recorridos:");
let litrosConsumidos = prompt("Ingrese los litros consumidos:");
// Calcular el consumo de combustible
let consumo = KmRecorridos / litrosConsumidos;
// Mostrar el resultado
alert("El consumo de combustible es: " + consumo + " km/litro");
console.log("El consumo de combustible es: " + consumo + " km/litro");