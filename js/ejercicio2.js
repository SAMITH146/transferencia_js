
// Ejercicio 2: Calcular el salario semanal de un trabajador

// Solicitar al usuario las horas trabajadas y el valor por hora
let horas = prompt("Ingrese horas trabajadas:");
let valorHora = prompt("Ingrese el valor por hora:");
// Calcular el salario semanal
let salario = horas * valorHora;
// Mostrar el resultado
alert("El salario semanal es: " + salario);
console.log("El salario semanal es: " + salario);
