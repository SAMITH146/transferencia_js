// Ejercicio 5: Calcular el tiempo estimado de viaje

// Solicitar al usuario la distancia y la velocidad promedio
let distancia = prompt("Ingrese distancia (km):");
let velocidad = prompt("Ingrese velocidad promedio (km/h):");
// Calcular el tiempo estimado de viaje
let tiempo = distancia / velocidad;
// Mostrar el resultado
alert("Tiempo estimado: " + tiempo + " horas");
console.log("Tiempo estimado: " + tiempo + " horas");